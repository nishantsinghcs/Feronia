            <b><h2>ASHTANGA of AYURVEDA</h2></b><br><br>
            In Sanskrit literature , Ayurveda was called “the science of eight components”-<br><br>
           <h3>   1.  kaya chikitsa (general medicine)<br><br>
2.  Shalya tantra (surgery)<br><br>
3.  Bhuta vidya (psychiatry)<br><br>
4.  Shalakya tantra (ENT)<br><br>
5.  Agada tantra (toxicology)<br><br>
6.  Vajikarana (purification of genetic organs)<br><br>
7.  Rasayana tantra (health and longevity)<br><br>
8.  Kaumar bhritya(Pediatrics)<br><br>
</h3>
       <br><br><br>
            <b><h2>TRIDOSHA IN AYURVEDA</h2></b><br><br> 
            <h3>    
            Ayurveda states a balance of the there elemental substances, the doshas- vata, pitta and kafa. 
            One ayurvedic theory states that each human possesses a unique combination of these three doshas which define this person’s
             temperament and characteristics. Each person has a natural state or natural combination of these three elements,
              and should seek balance by modulating their behavior or environment.
             I this way they can increase or decrease the doshas they lack or they have an abundance respectively.
</h3>

<br><br><br>
            <b><h2>SOME CONCEPTS OF AYURVEDA</h2></b><br>
            <h3>    
            Ayurveda has historically taken the approach of enumerating bodily substances in the framework of the five classical elements known as panchmahabhoota- earth, water, fire, air and ether.
Moreover, Ayurveda names seven basic tissues (dhatu). They are rasa (plasma), rakta (blood), mansa (muscles), meda (fat), asthi (bone), mazza (marrow) and shukra (semen) and also a very important dhatu, which is known as “oja”.  
Except it there are three gunas- rajaguna, tamoguna and stvaguna which determine the nature of the person according to the behavior.
<br><br></br>
There are three major ayurvedic texts- charak samhita, shushruta samhita and the ashtangha hridaya samhita. 
</h3><br><br> <br><br> 
<b><h2>1.  SHUSHRUTA SAMHITA</h2></b><br>
            <h3>    
            The most authentic compilation of shushruta’s teachings contains 184 chapters and description of 1,120 illness, 
            700 medicinal plants, 64 preparations from mineral sources and 57 preparations based on animal sources. 

</h3><br><br>
<b><h2>2.  CHARAKA SAMHITA</h2></b><br>
            <h3>    
            written by charaka is arguably the principle classic reference. 
            It gives emphasis to the triune nature of each person: body care,
             mental regulation and spiritual refinement.  

</h3><br><br> <br><br> 
<b><h2>3.  ASHTANGHA HRIDAYA</h2></b><br>
            <h3>    
            It is a concise version of the works of charaka and shushruta.

</h3>
